/**
 * Operations class
 * @author juanm
 *
 */
public class Operaciones {

	//Global variables
	double num1, num2; //Numbers
	double result; //Results
	
	//addition function
	double suma() {
		
		result = num1 + num2; //Operation result stores in variable
		
		return result; //Returns its value
		
	}
	
	//subtraction function
	double resta() {
		
		result = num1 - num2; //Operation result stores in variable
		
		return result;//Returns its value
		
	}
	
	//multiplication function
	double multiplicacion() {
		
		result = num1 * num2; //Operation result stores in variable
		
		return result;//Returns its value
		
	}
	
	//division function
	double division() {
		
		result = num1 / num2; //Operation result stores in variable
		
		return result;//Returns its value
		
	}
	
}
