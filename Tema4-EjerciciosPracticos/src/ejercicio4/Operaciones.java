package ejercicio4;

public class Operaciones {

	double num1, num2;
	double result;
	
	double suma() {
		
		result = num1 + num2;
		
		return result;
		
	}
	
	double resta() {
		
		result = num1 - num2;
		
		return result;
		
	}
	
	double multiplicacion() {
		
		result = num1 * num2;
		
		return result;
		
	}
	
	double division() {
		
		result = num1 / num2;
		
		return result;
		
	}
	
}
